    private void delVehicleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delVehicleButtonActionPerformed
        if(Home.msgIsNotSelectedFila(tblMySavedPlaces)){
            Home.msgNotSelected("Seleccione un Vehiculo");
            System.out.println("***no selected");
        }else{
            int vehicleId= Integer.parseInt(tblMySavedPlaces.getValueAt(tblMySavedPlaces.getSelectedRow(), 0).toString());
            User user = UserData.getActiveUser();
            int resp=Home.optionMsg("Seguro que desea eliminar?");
            if(resp == JOptionPane.YES_OPTION){
                user.delVehicle(vehicleId);
                modelo.removeRow(tblMySavedPlaces.getSelectedRow());
                System.out.println("***Delete"); 
            }   
        }
    }//GEN-LAST:event_delVehicleButtonActionPerformed

    private void editVehicleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editVehicleButtonActionPerformed
        if(tblMySavedPlaces.getSelectedRow()<0){
            Home.msgNotSelected("Seleccione un Vehiculo");
        }else{
            int vehicleId= Integer.parseInt(tblMySavedPlaces.getValueAt(tblMySavedPlaces.getSelectedRow(), 0).toString());
            User user = UserData.getActiveUser();
            Vehicle selectedVehicle = user.searchVehicleID(vehicleId);
            new VSavedVehiclesPanelEditScreen(selectedVehicle, user).setVisible(true);
        }
        
        
    }//GEN-LAST:event_editVehicleButtonActionPerformed

